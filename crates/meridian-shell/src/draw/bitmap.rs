use meridian_config::Color;

use super::painter::Painter;

pub(super) fn draw_bitmap_text(
    painter: &mut Painter<'_>,
    text: &str,
    x: i32,
    y: i32,
    max_w: i32,
    color: Color,
) {
    let mut cursor = x;
    for ch in text.chars() {
        if cursor + 6 > x + max_w {
            break;
        }
        let glyph = bitmap_glyph(ch);
        for (row, bits) in glyph.iter().enumerate() {
            for col in 0..5 {
                if bits & (1 << (4 - col)) != 0 {
                    painter.blend_pixel(cursor + col, y + row as i32, color, 255);
                }
            }
        }
        cursor += 6;
    }
}

